/**
 * Build-time suite image optimizer (run in `npm run build` before `vite build`).
 *
 * For every photo under `src/assets/suites/**`, emits a display-sized WebP into
 * `public/suites/<same path>.webp` and records a tiny inlined blur placeholder.
 * The originals (full-resolution camera files) are NEVER modified — they stay on
 * disk for archival; only the lightweight derivatives are shipped.
 *
 * Resizing to ~2200px is visually lossless on screen: the gallery never displays
 * more than ~1100 CSS px (~2200 device px on a 2× display), so the discarded pixels
 * were never drawn. A 4284×5712 / ~5 MB JPEG becomes a ~2200px / ~0.4 MB WebP.
 *
 * Output: `src/generated/suiteImages.js` — `{ '<room>/<file>': { src, width, height, placeholder } }`.
 *
 *   node scripts/optimize-suite-images.mjs
 */
import { createHash } from 'node:crypto'
import { mkdir, readdir, rm, stat, writeFile } from 'node:fs/promises'
import { dirname, join, relative, sep } from 'node:path'
import { fileURLToPath } from 'node:url'
import sharp from 'sharp'

const __dirname = dirname(fileURLToPath(import.meta.url))
const siteRoot = join(__dirname, '..')
const srcDir = join(siteRoot, 'src/assets/suites')
const outDir = join(siteRoot, 'public/suites')
const manifestPath = join(siteRoot, 'src/generated/suiteImages.js')

/** Long-edge cap — covers a ~1100px frame at 2× DPR with headroom. */
const MAX_EDGE = 2200
const WEBP_QUALITY = 82
const PLACEHOLDER_EDGE = 24

const IMG_RE = /\.(jpe?g|png|webp)$/i

async function walk(dir) {
  const out = []
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name)
    if (entry.isDirectory()) out.push(...(await walk(full)))
    else if (IMG_RE.test(entry.name)) out.push(full)
  }
  return out
}

/** Encode each path segment so spaces/parens (e.g. `IMG_0580 (2)`) survive in a URL. */
function toUrlPath(relPosix) {
  return relPosix.split('/').map(encodeURIComponent).join('/')
}

await rm(outDir, { recursive: true, force: true })
await mkdir(dirname(manifestPath), { recursive: true })

const files = (await walk(srcDir)).sort()
const manifest = {}
let bytesIn = 0
let bytesOut = 0

for (const file of files) {
  const relKey = relative(srcDir, file).split(sep).join('/') // presidential/IMG_0543.JPG
  const relWebp = relKey.replace(IMG_RE, '.webp')
  const outFile = join(outDir, relWebp.split('/').join(sep))

  const optimized = await sharp(file)
    .rotate()
    .resize({ width: MAX_EDGE, height: MAX_EDGE, fit: 'inside', withoutEnlargement: true })
    .webp({ quality: WEBP_QUALITY, effort: 6 })
    .toBuffer({ resolveWithObject: true })

  await mkdir(dirname(outFile), { recursive: true })
  await writeFile(outFile, optimized.data)

  const placeholder = await sharp(file)
    .rotate()
    .resize({ width: PLACEHOLDER_EDGE, height: PLACEHOLDER_EDGE, fit: 'inside' })
    .webp({ quality: 40 })
    .toBuffer()

  const hash = createHash('sha1').update(optimized.data).digest('hex').slice(0, 8)

  manifest[relKey] = {
    src: `/suites/${toUrlPath(relWebp)}?v=${hash}`,
    width: optimized.info.width,
    height: optimized.info.height,
    placeholder: `data:image/webp;base64,${placeholder.toString('base64')}`,
  }

  bytesIn += (await stat(file)).size
  bytesOut += optimized.data.length
}

const body =
  '// AUTO-GENERATED by scripts/optimize-suite-images.mjs — do not edit by hand.\n' +
  '// Display-sized WebP URL + inlined blur placeholder for each suite photo.\n' +
  `export default ${JSON.stringify(manifest, null, 2)}\n`
await writeFile(manifestPath, body)

const mb = (n) => `${(n / 1024 / 1024).toFixed(1)} MB`
console.log(
  `Optimized ${files.length} suite images: ${mb(bytesIn)} → ${mb(bytesOut)} ` +
    `(public/suites, manifest: src/generated/suiteImages.js)`,
)
